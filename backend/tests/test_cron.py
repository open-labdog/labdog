class TestCronValidator:
    def test_basic_cron_valid(self):
        result = validate_cron_expression("0 2 * * *")
        assert result == ("0", "2", "*", "*", "*")

    def test_step_notation_valid(self):
        result = validate_cron_expression("*/5 * * * *")
        assert result == ("*/5", "*", "*", "*", "*")

    def test_list_notation_valid(self):
        result = validate_cron_expression("0 0 1,15 * *")
        assert result == ("0", "0", "1,15", "*", "*")

    def test_range_notation_valid(self):
        result = validate_cron_expression("0 9-17 * * 1-5")
        assert result == ("0", "9-17", "*", "*", "1-5")

    def test_too_many_fields_rejected(self):
        with pytest.raises(ValueError, match="exactly 5 fields"):
            validate_cron_expression("0 * * * * *")

    def test_too_few_fields_rejected(self):
        with pytest.raises(ValueError, match="exactly 5 fields"):
            validate_cron_expression("* * * *")

    def test_at_special_rejected(self):
        with pytest.raises(ValueError, match="Special schedules"):
            validate_cron_expression("@daily")

    def test_minute_out_of_range(self):
        with pytest.raises(ValueError, match="Invalid minute"):
            validate_cron_expression("60 * * * *")

    def test_hour_out_of_range(self):
        with pytest.raises(ValueError, match="Invalid hour"):
            validate_cron_expression("* 25 * * *")


