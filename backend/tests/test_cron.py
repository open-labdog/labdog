class TestCronCollector:
    def test_ansible_marker_parsing(self):
        crontab = (
            "# DO NOT EDIT THIS FILE - edit the master and reinstall.\n"
            "#Ansible: daily-backup\n"
            "0 2 * * * /usr/local/bin/backup.sh\n"
            "#Ansible: log-rotate\n"
            "0 0 * * 0 /usr/sbin/logrotate /etc/logrotate.conf\n"
        )
        entries = _parse_crontab(crontab, "root")
        assert len(entries) == 2
        assert entries[0]["name"] == "daily-backup"
        assert entries[0]["schedule"] == "0 2 * * *"
        assert entries[0]["command"] == "/usr/local/bin/backup.sh"
        assert entries[0]["user"] == "root"
        assert entries[1]["name"] == "log-rotate"
        assert entries[1]["schedule"] == "0 0 * * 0"

    def test_non_managed_entries_skipped(self):
        crontab = (
            "# crontab for deploy\n"
            "*/10 * * * * /usr/bin/check-health\n"
            "0 3 * * * /usr/bin/manual-job\n"
            "#Ansible: managed-task\n"
            "30 4 * * * /opt/scripts/run.sh\n"
        )
        entries = _parse_crontab(crontab, "deploy")
        assert len(entries) == 1
        assert entries[0]["name"] == "managed-task"
        assert entries[0]["user"] == "deploy"
        assert entries[0]["schedule"] == "30 4 * * *"
        assert entries[0]["command"] == "/opt/scripts/run.sh"

    def test_empty_crontab_returns_empty(self):
        entries = _parse_crontab("", "root")
        assert entries == []

    def test_marker_without_following_line_skipped(self):
        crontab = "#Ansible: orphan-marker\n"
        entries = _parse_crontab(crontab, "root")
        assert entries == []


