class TestCronAPI:
    @pytest.mark.asyncio
    async def test_create_group_cron_job(self, superuser_client, db):
        from tests.conftest import create_group

        group = await create_group(db)
        await db.commit()
        resp = await superuser_client.post(
            f"/api/groups/{group.id}/cron-jobs",
            json={
                "name": "daily-backup",
                "schedule": "0 2 * * *",
                "command": "/usr/bin/backup",
            },
        )
        assert resp.status_code == 201
        data = resp.json()
        assert data["name"] == "daily-backup"
        assert data["group_id"] == group.id
        assert data["user"] == "root"
        assert data["state"] == "present"

    @pytest.mark.asyncio
    async def test_invalid_schedule_rejected_by_api(self, superuser_client, db):
        from tests.conftest import create_group

        group = await create_group(db)
        await db.commit()
        resp = await superuser_client.post(
            f"/api/groups/{group.id}/cron-jobs",
            json={
                "name": "bad-schedule",
                "schedule": "@daily",
                "command": "/bin/true",
            },
        )
        assert resp.status_code == 422

    @pytest.mark.asyncio
    async def test_list_group_cron_jobs(self, superuser_client, db):
        from tests.conftest import create_group

        group = await create_group(db)
        await db.commit()
        await superuser_client.post(
            f"/api/groups/{group.id}/cron-jobs",
            json={"name": "job-a", "schedule": "0 1 * * *", "command": "/bin/a"},
        )
        await superuser_client.post(
            f"/api/groups/{group.id}/cron-jobs",
            json={"name": "job-b", "schedule": "0 2 * * *", "command": "/bin/b"},
        )
        resp = await superuser_client.get(f"/api/groups/{group.id}/cron-jobs")
        assert resp.status_code == 200
        names = [j["name"] for j in resp.json()]
        assert "job-a" in names
        assert "job-b" in names

    @pytest.mark.asyncio
    async def test_effective_cron_merge(self, superuser_client, db):
        from tests.conftest import create_group, create_host, create_ssh_key

        group = await create_group(db)
        ssh_key = await create_ssh_key(db)
        host = await create_host(db, ssh_key_id=ssh_key.id, group_ids=[group.id])
        await db.commit()

        await superuser_client.post(
            f"/api/groups/{group.id}/cron-jobs",
            json={
                "name": "deploy-job",
                "schedule": "0 3 * * *",
                "command": "/bin/deploy-group",
            },
        )
        await superuser_client.post(
            f"/api/hosts/{host.id}/cron-jobs",
            json={
                "name": "deploy-job",
                "schedule": "0 4 * * *",
                "command": "/bin/deploy-host",
            },
        )

        resp = await superuser_client.get(f"/api/hosts/{host.id}/effective-cron-jobs")
        assert resp.status_code == 200
        data = resp.json()
        deploy = [j for j in data if j["name"] == "deploy-job"]
        assert len(deploy) == 1
        assert deploy[0]["source"] == "host"
        assert deploy[0]["command"] == "/bin/deploy-host"
        assert deploy[0]["schedule"] == "0 4 * * *"
