class TestCronSchemas:
    def test_invalid_slug_rejected(self):
        with pytest.raises(ValidationError, match="Invalid cron job name"):
            CronJobCreate(
                name="my backup",
                schedule="0 2 * * *",
                command="/usr/bin/backup",
            )

    def test_valid_slug_accepted(self):
        job = CronJobCreate(
            name="daily-backup",
            schedule="0 2 * * *",
            command="/usr/bin/backup",
        )
        assert job.name == "daily-backup"

    def test_invalid_schedule_rejected(self):
        with pytest.raises(ValidationError, match="Special schedules"):
            CronJobCreate(
                name="hourly-task",
                schedule="@hourly",
                command="/usr/bin/task",
            )

    def test_empty_command_rejected(self):
        with pytest.raises(ValidationError, match="command must not be empty"):
            CronJobCreate(
                name="no-cmd",
                schedule="* * * * *",
                command="   ",
            )

    def test_defaults_applied(self):
        job = CronJobCreate(
            name="test-job",
            schedule="0 * * * *",
            command="/bin/true",
        )
        assert job.user == "root"
        assert job.state == "present"
        assert job.priority == 0
        assert job.environment == {}


